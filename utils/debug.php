<?php

/**
 * =====================================================================
 * debug.php — Управление выводом ошибок PHP
 * =====================================================================
 *
 * Назначение:
 *   Единая точка управления отображением ошибок PHP во всём проекте.
 *   Вместо того чтобы настраивать error_reporting в каждом файле,
 *   все скрипты просто подключают этот файл через require_once.
 *
 * Что делает:
 *   1. Включает вывод ошибок на экран (для разработки и отладки)
 *   2. Включает вывод ошибок, возникающих на этапе запуска скрипта
 *   3. Устанавливает максимальный уровень детализации ошибок (E_ALL)
 *
 * Раскомментировать для вывода ошибок на экран
 */

require_once 'configs/settings.php';

$settings = getSettings();
if ($settings['debug']) {
    ini_set('display_errors', 1);
    ini_set('display_startup_errors', 1);
    error_reporting(E_ALL);
}
