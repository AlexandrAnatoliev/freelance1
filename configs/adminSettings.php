<?php

require_once 'vendor/autoload.php';

// загрузка .env файла
$dotenv = Dotenv\Dotenv::createImmutable(__DIR__);
$dotenv->load();

/**
 * Возвращает настройки администратора в виде ассоциативного массива.
 *
 * Функция пытается прочитать значения из переменных окружения
 * (загруженных из .env файла). Если переменная не найдена или
 * пуста — подставляет значение по умолчанию.
 *
 * Структура возвращаемого массива:
 *   [
 *     'email' => string  Email администратора для получения
 *                        копий счетов и уведомлений.
 *                        По умолчанию: 'admin@gmail.com'
 *
 *     'name'  => string  Имя администратора, отображается
 *                        на странице результата заказа и в
 *                        письмах как получатель копии.
 *                        По умолчанию: 'Администратор'
 *   ]
 *
 * @return array  Ассоциативный массив с ключами 'email' и 'name'
 */
function getAdminSettings(): array
{
    return [
        'email' => $_ENV['ADMIN_EMAIL'] ?? 'admin@gmail.com',
        'name'  => $_ENV['ADMIN_NAME'] ?? 'Администратор',
    ];
}
