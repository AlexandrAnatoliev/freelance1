
<?php
require_once 'vendor/autoload.php';

$dotenv = Dotenv\Dotenv::createImmutable(__DIR__);
$dotenv->load();

/**
 * Безопасно получает значение переменной окружения с цепочкой fallback'ов.
 *
 * Эта вспомогательная функция нужна, чтобы избежать ситуации,
 * когда в счёте на оплату оказывается пустое поле реквизитов.
 * Она последовательно проверяет три источника и возвращает
 * первое непустое значение, либо строку-заглушку.
 *
 * Порядок проверки (приоритет):
 *   1. $_ENV[$key]   - переменная окружения из .env (основной источник)
 *   2. getenv($key)  - переменная окружения системного уровня (запасной)
 *   3. $default      - строка-заглушка "Заполнить настройки!"
 *                      (подсказывает администратору, что реквизиты не настроены)
 *
 * @param $key      - Ключ переменной окружения.
 *                    Например: 'BANK_DETAILS_RECIPIENT_BANK'
 * @param $default  - Значение по умолчанию, если переменная
 *                    не найдена или пуста ни в одном источнике.
 *                    По умолчанию: 'Заполнить настройки!'
 * @return Значение переменной окружения или строку по умолчанию.
 *         Никогда не возвращает пустую строку.
 */
function envOr(string $key, string $default = 'Заполнить настройки!'): string
{
    return $_ENV[$key] ?? getenv($key) ?: $default;
}

/**
 * Возвращает полный набор банковских реквизитов продавца
 * для заполнения счёта на оплату.
 *
 * Функция собирает все реквизиты в один ассоциативный массив.
 * Каждое значение получается через envOr(), поэтому даже при
 * отсутствии .env файла счёт будет содержать строки-заглушки,
 * а не пустые поля.
 *
 * Структура возвращаемого массива:
 *   [
 *     'recipient_bank'             => string  Полное название банка получателя.
 *                                             Пример: 'ФИЛИАЛ "ИВАНОВСКИЙ" АО "ИВАН-БАНК" г. Иваново'
 *                                             В счёте: поле "Банк получателя"
 *     'bank_identification_code'   => string  БИК банка (Банковский Идентификационный Код).
 *                                             Пример: '123456789'
 *                                             В счёте: поле "БИК"
 *     'correspondent_bank_account' => string  Корреспондентский счёт банка.
 *                                             Пример: '30101000000000000000'
 *                                             В счёте: поле "Сч. №" (банка)
 *     'recipients_bank_account'    => string  Расчётный счёт получателя (ИП/компании).
 *                                             Пример: '40802000000000000000'
 *                                             В счёте: поле "Сч. №" (получателя)
 *     'ip_name'                    => string  Краткое наименование ИП/организации.
 *                                             Пример: 'ИП Иванов Иван'
 *                                             В счёте: поле "Получатель"
 *     'ip_full_name'               => string  Полные реквизиты ИП/организации с адресом и телефоном.
 *                                             Пример: 'ИП Иванов Иван, ИНН ..., адрес, тел.'
 *                                             В счёте: поле "Поставщик (Исполнитель)"
 *     'payment_basis'              => string  Основание платежа (договор, счёт и т.д.).
 *                                             Пример: 'Основной договор'
 *                                             В счёте: поле "Основание"
 *     'entrepreneurs_surname'      => string  Фамилия предпринимателя для подписи.
 *                                             Пример: 'Иванов И.'
 *                                             В счёте: строка подписи "Предприниматель___________"
 *   ]
 *
 * @return Ассоциативный массив со всеми банковскими реквизитами.
 *         Все значения — строки.
 */
function getBankDetailsSettings(): array
{
    return [
        'recipient_bank'             => envOr('BANK_DETAILS_RECIPIENT_BANK'),
        'bank_identification_code'   => envOr('BANK_DETAILS_BANK_IDENTIFICATION_CODE'),
        'correspondent_bank_account' => envOr('BANK_DETAILS_CORRESPONDENT_BANK_ACCOUNT'),
        'recipients_bank_account'    => envOr('BANK_DETAILS_RECIPIENTS_BANK_ACCOUNT'),
        'ip_name'                    => envOr('BANK_DETAILS_IP_NAME'),
        'ip_full_name'               => envOr('BANK_DETAILS_IP_FULL_NAME'),
        'payment_basis'              => envOr('BANK_DETAILS_PAYMENT_BASIS'),
        'entrepreneurs_surname'      => envOr('BANK_DETAILS_ENTREPRENEURS_SURNAME'),
    ];
}
